const Button = ({ percentage, tipPrecentage, setTipPercentage }) => {
  return (
    <button
      // ????????????????????????????????????????????????????????????
      className={tipPrecentage === percentage ? "selected-btn" : null}
      onClick={() => setTipPercentage(percentage)}
      type="button"
    >
      {percentage}%
    </button>
  );
};

export default Button;
